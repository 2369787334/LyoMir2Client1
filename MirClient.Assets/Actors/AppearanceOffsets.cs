    public static int GetNpcOffset(int appearance)
    {
        if (appearance < 0)
            return 0;

        return appearance switch
        {
            >= 0 and <= 22 => appearance * 60,
            23 => 1380,
            24 or 25 => (appearance - 24) * 60 + 1470,
            27 or 32 => (appearance - 26) * 60 + 1620 - 30,
            26 or 28 or 29 or 30 or 31 or >= 33 and <= 41 => (appearance - 26) * 60 + 1620,
            42 or 43 => 2580,
            >= 44 and <= 47 => 2640,
            >= 48 and <= 50 => (appearance - 48) * 60 + 2700,
            51 => 2880,
            52 => 2960,
            >= 54 and <= 58 => 4490 + 10 * (appearance - 54),
            >= 94 and <= 98 => 4490 + 10 * (appearance - 94),
            59 => 4540,
            >= 60 and <= 67 => 3060 + 60 * (appearance - 60),
            68 => 3600,
            >= 70 and <= 75 => 3780 + 10 * (appearance - 70),
            76 or 77 => 3840 + 60 * (appearance - 76),
            >= 81 and <= 83 => 3960 + 20 * (appearance - 81),
            >= 78 and <= 80 => 4060 + 60 * (appearance - 78),
            84 => 4030,
            85 => 4250,
            >= 90 and <= 92 => 3750 + 10 * (appearance - 90),
            >= 100 and <= 123 => appearance switch
            {
                111 => 740,
                112 => 810,
                113 => 820,
                114 => 830,
                115 => 840,
                116 => 850,
                117 => 860,
                118 => 870,
                119 => 900,
                120 => 930,
                121 => 970,
                122 => 980,
                123 => 990,
                _ => (appearance - 100) * 70
            },
            130 => 4240,
            131 => 4560,
            132 => 4770,
            133 => 4810,
            _ => 0
        };
    }
